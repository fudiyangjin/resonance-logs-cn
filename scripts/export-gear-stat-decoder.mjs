import { createHash } from "node:crypto";
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..");
const proofCsvPath = path.join(repoRoot, "DEV_exports", "gear-stat-proof.csv");
const observedCsvPath = path.join(repoRoot, "DEV_exports", "gear-observed-attributes.csv");
const outputPath = path.join(repoRoot, "src", "lib", "gear-stat-decoder.generated.ts");

const validLanes = new Set(["legendary-affix", "advanced-attribute", "locked"]);
const validProofStatuses = new Set(["basic-attr-value-match", "value-match", "structural-match", "grow-slot-only"]);
const validConfidence = new Set(["high", "medium", "low"]);
const validValueKinds = new Set(["flat", "percent", "locked", "unknown"]);

const proofRows = readCsvObjects(proofCsvPath);
const observedRows = readCsvObjects(observedCsvPath);
const observedValueKinds = buildObservedValueKindLookup(observedRows);

const seedRows = proofRows
  .filter((row) => row.finalLabel?.trim())
  .map((row) => toSeedRow(row, observedValueKinds))
  .sort((left, right) => {
    if (left.itemId !== right.itemId) return left.itemId - right.itemId;
    return left.pairSlot - right.pairSlot;
  });

const sourceHash = createHash("sha256")
  .update(readFileSync(proofCsvPath))
  .update(readFileSync(observedCsvPath))
  .digest("hex")
  .slice(0, 12);

const generatedSource = {
  version: `proof-${sourceHash}`,
  sourceFiles: ["DEV_exports/gear-stat-proof.csv", "DEV_exports/gear-observed-attributes.csv"],
  rowCount: seedRows.length,
  observedValueRowCount: seedRows.filter((row) => row.source === "observed-proof").length,
};

const output = `// Generated by scripts/export-gear-stat-decoder.mjs. Do not edit by hand.
import type { GearStatSeedLine } from "./gear-stat-decoder";

export const GEAR_STAT_DECODER_SOURCE = ${JSON.stringify(generatedSource, null, 2)} as const;

export const GEAR_STAT_DECODER_SEED = ${JSON.stringify(seedRows, null, 2)} as const satisfies readonly GearStatSeedLine[];
`;

writeFileSync(outputPath, output);
console.log(`wrote ${path.relative(repoRoot, outputPath)} (${seedRows.length} rows, ${generatedSource.version})`);

function toSeedRow(row, observedValueKinds) {
  const itemId = requiredInteger(row, "itemId");
  const pairSlot = requiredInteger(row, "pairSlot");
  const lane = requiredString(row, "lane");
  const label = requiredString(row, "finalLabel");
  const locked = lane === "locked" || label.toLowerCase() === "locked";
  const value = locked ? null : optionalNumber(row, "observedValue");
  const valueKind = normalizeValueKind(
    locked ? "locked" : value === null ? "unknown" : observedValueKinds.get(`${itemId}:${pairSlot}`) ?? inferValueKind(value),
  );

  return {
    itemId,
    itemName: requiredString(row, "itemName"),
    wearingLevel: requiredInteger(row, "wearingLevel"),
    pairSlot,
    pairId: requiredInteger(row, "pairId"),
    attrLibId: requiredInteger(row, "attrLibId"),
    attrLibIndex: requiredString(row, "attrLibIndex"),
    label,
    value,
    valueKind,
    lane: normalizeLane(lane),
    basicGrowRangeId: requiredInteger(row, "basicGrowRangeId"),
    basicAttrGrowRowId: requiredInteger(row, "basicAttrGrowRowId"),
    proofStatus: normalizeProofStatus(requiredString(row, "proofStatus")),
    proofConfidence: normalizeConfidence(requiredString(row, "proofConfidence")),
    evaluatorConfidence: normalizeConfidence(requiredString(row, "evaluatorConfidence")),
    source: locked ? "locked-lane" : value === null ? "inferred-proof" : "observed-proof",
  };
}

function buildObservedValueKindLookup(rows) {
  const lookup = new Map();

  for (const row of rows) {
    const itemId = optionalInteger(row, "itemId");
    const pairSlot = optionalInteger(row, "pairSlot");
    const valueKind = row.valueKind?.trim();

    if (itemId === null || pairSlot === null || !valueKind || !validValueKinds.has(valueKind)) {
      continue;
    }

    lookup.set(`${itemId}:${pairSlot}`, valueKind);
  }

  return lookup;
}

function inferValueKind(value) {
  return Number.isInteger(value) ? "flat" : "percent";
}

function normalizeLane(value) {
  if (validLanes.has(value)) {
    return value;
  }

  throw new Error(`Unsupported gear stat lane: ${value}`);
}

function normalizeProofStatus(value) {
  if (validProofStatuses.has(value)) {
    return value;
  }

  throw new Error(`Unsupported gear stat proof status: ${value}`);
}

function normalizeConfidence(value) {
  if (validConfidence.has(value)) {
    return value;
  }

  throw new Error(`Unsupported gear stat confidence: ${value}`);
}

function normalizeValueKind(value) {
  if (validValueKinds.has(value)) {
    return value;
  }

  throw new Error(`Unsupported gear stat value kind: ${value}`);
}

function requiredString(row, key) {
  const value = row[key]?.trim();
  if (!value) {
    throw new Error(`Missing required CSV field ${key}`);
  }
  return value;
}

function optionalInteger(row, key) {
  const raw = row[key]?.trim();
  if (!raw) return null;

  const value = Number(raw);
  if (!Number.isFinite(value)) {
    throw new Error(`Invalid integer field ${key}: ${raw}`);
  }

  return Math.trunc(value);
}

function requiredInteger(row, key) {
  const value = optionalInteger(row, key);
  if (value === null) {
    throw new Error(`Missing required integer field ${key}`);
  }
  return value;
}

function optionalNumber(row, key) {
  const raw = row[key]?.trim();
  if (!raw) return null;

  const value = Number(raw);
  if (!Number.isFinite(value)) {
    throw new Error(`Invalid number field ${key}: ${raw}`);
  }

  return value;
}

function readCsvObjects(filePath) {
  const rows = parseCsv(readFileSync(filePath, "utf8"));
  const header = rows.shift();

  if (!header) {
    throw new Error(`CSV has no header: ${filePath}`);
  }

  return rows
    .filter((row) => row.some((cell) => cell.trim()))
    .map((row) =>
      Object.fromEntries(header.map((key, index) => [key, row[index] ?? ""])),
    );
}

function parseCsv(text) {
  const rows = [];
  let row = [];
  let cell = "";
  let quoted = false;

  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];
    const next = text[index + 1];

    if (quoted) {
      if (char === '"' && next === '"') {
        cell += '"';
        index += 1;
      } else if (char === '"') {
        quoted = false;
      } else {
        cell += char;
      }
      continue;
    }

    if (char === '"') {
      quoted = true;
      continue;
    }

    if (char === ",") {
      row.push(cell);
      cell = "";
      continue;
    }

    if (char === "\n") {
      row.push(cell);
      rows.push(row);
      row = [];
      cell = "";
      continue;
    }

    if (char !== "\r") {
      cell += char;
    }
  }

  if (cell.length > 0 || row.length > 0) {
    row.push(cell);
    rows.push(row);
  }

  return rows;
}
