const root = path.join(process.cwd(), "src");
const zhLiteralPath = path.join(root, "lib", "i18n", "locales", "zh-CN", "literal.ts");
const enLiteralPath = path.join(root, "lib", "i18n", "locales", "en", "literal.ts");

function shouldSkip(fullPath) {
  const normalized = fullPath.split(path.sep).join("/");
  if (!/\.(svelte|ts)$/.test(fullPath)) return true;
  if (normalized.includes("/src/lib/i18n/")) return true;
  if (normalized.includes("/src-tauri/")) return true;
  if (normalized.endsWith("/src/lib/config/buff-name-table.ts")) return true;
  if (normalized.endsWith(".json")) return true;
  return false;
}

function walk(dir, files = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      walk(fullPath, files);
      continue;
    }
    if (!shouldSkip(fullPath)) {
      files.push(fullPath);
    }
  }
  return files;
}

function replaceAllSourceLiterals() {
  const replacements = Object.entries(ZH_TO_EN).sort((a, b) => b[0].length - a[0].length);
  for (const filePath of walk(root)) {
    let source = fs.readFileSync(filePath, "utf8");
    let next = source;
    for (const [zh, en] of replacements) {
      next = next.split(zh).join(en);
    }
    if (next !== source) {
      fs.writeFileSync(filePath, next, "utf8");
    }
  }
}

function writeLiteralLocaleFiles() {
  const enToZh = { ...MANUAL_EN_TO_ZH };
  for (const [zh, en] of Object.entries(ZH_TO_EN)) {
    enToZh[en] = zh;
  }
  const sortedEntries = Object.entries(enToZh).sort((a, b) => a[0].localeCompare(b[0], "en"));
  const zhBody = sortedEntries
    .map(([en, zh]) => `  ${JSON.stringify(en)}: ${JSON.stringify(zh)},`)
    .join("\n");
  const zhSource = `const literal = {\n${zhBody}\n};\n\nexport default literal;\n`;
  const enSource = `import zhLiteral from "../zh-CN/literal";\n\nconst literal = Object.fromEntries(\n  Object.keys(zhLiteral).map((key) => [key, key]),\n) as Record<string, string>;\n\nexport default literal;\n`;
  fs.writeFileSync(zhLiteralPath, zhSource, "utf8");
  fs.writeFileSync(enLiteralPath, enSource, "utf8");
}

replaceAllSourceLiterals();
writeLiteralLocaleFiles();
