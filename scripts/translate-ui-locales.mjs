function toTraditional(value) {
  let out = value;
  for (const [source, target] of zhTwPhrases) {
    out = out.split(source).join(target);
  }
  out = Array.from(out, (char) => zhTwChars.get(char) ?? char).join("");
  return out;
}

function applyPhraseMap(value, locale) {
  let out = value;
  for (const [source, target] of phraseMaps[locale]) {
    out = out.split(source).join(target);
  }
  return out;
}

function fallbackTranslate(value, locale) {
  if (keepAsIs.has(value)) {
    return value;
  }

  const mapped = fallbackWords[locale][value];
  if (mapped) {
    return mapped;
  }

  let out = applyPhraseMap(value, locale);

  for (const [source, target] of Object.entries(fallbackWords[locale])) {
    const pattern = new RegExp(`\\b${source.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")}\\b`, "g");
    out = out.replace(pattern, target);
  }

  return out;
}

function translateValue(value, zhValue, locale) {
  if (locale === "zh-TW") {
    const source = zhValue ?? value;
    if (exactTw.has(source)) {
      return exactTw.get(source);
    }
    if (exactTw.has(value)) {
      return exactTw.get(value);
    }
    return toTraditional(source);
  }

  if (exact.has(value)) {
    return exact.get(value)[locale];
  }

  if (typeof zhValue === "string" && exactZh.has(zhValue)) {
    return exactZh.get(zhValue)[locale];
  }

  return fallbackTranslate(value, locale);
}

function translateObject(rel, locale) {
  const enPath = path.join(root, "en", "ui", rel);
  const zhPath = path.join(root, "zh-CN", "ui", rel);
  const enData = JSON.parse(fs.readFileSync(enPath, "utf8"));
  const zhData = fs.existsSync(zhPath) ? JSON.parse(fs.readFileSync(zhPath, "utf8")) : {};
  const out = {};

  for (const [key, value] of Object.entries(enData)) {
    out[key] = translateValue(value, zhData[key], locale);
  }

  return out;
}

function writeLocale(locale) {
  for (const rel of uiFiles) {
    const outPath = path.join(root, locale, "ui", rel);
    fs.mkdirSync(path.dirname(outPath), { recursive: true });
    const data = translateObject(rel, locale);
    fs.writeFileSync(outPath, `${JSON.stringify(data, null, 2)}\n`, "utf8");
  }
}

for (const locale of ["zh-TW", "th", "id"]) {
  writeLocale(locale);
}

const copiedValues = { th: 0, id: 0 };
const copiedSamples = { th: [], id: [] };

for (const rel of uiFiles) {
  const enData = JSON.parse(fs.readFileSync(path.join(root, "en", "ui", rel), "utf8"));
  for (const locale of ["th", "id"]) {
    const data = JSON.parse(fs.readFileSync(path.join(root, locale, "ui", rel), "utf8"));
    for (const [key, value] of Object.entries(data)) {
      const enValue = enData[key];
      if (value === enValue && !keepAsIs.has(value) && !/^[A-Z0-9 ._:/+%(){}-]+$/.test(value)) {
        copiedValues[locale] += 1;
        if (copiedSamples[locale].length < 15) {
          copiedSamples[locale].push(`${rel}:${key}=${value}`);
        }
      }
    }
  }
}

